func fibo(num: Int) -> Int {
    print("Calculating fibonacci(\(num))...")
    if (num <= 2) {
        print("Terminal case, returning 1.")
        return 1
    }
    else {
        let run = fibo(num: num-1) + fibo(num: num-2)
        print("Non-terminal case, returning \(run).")
        return run
    }
}

